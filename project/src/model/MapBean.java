// DTO(Data Transfer Object)

package model;

public class MapBean {
	private int store_code;
	private float x_code;
	private float y_code;
	
	public int getStore_code() {
		return store_code;
	}
	public float getX_code() {
		return x_code;
	}
	public float getY_code() {
		return y_code;
	}
	public void setStore_code(int store_code) {
		this.store_code = store_code;
	}
	public void setX_code(float x_code) {
		this.x_code = x_code;
	}
	public void setY_code(float y_code) {
		this.y_code = y_code;
	}
	
}
